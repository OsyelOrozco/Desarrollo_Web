function ingresarComo(rol) {
    if (rol === 'administrador') {
        window.location.href = 'login/login.html';
    } else if (rol === 'usuario') {
        window.location.href = 'login/login.html';
    } else if (rol === 'enfermera(o)') {
        window.location.href = 'login/login.html';
    } else if (rol === 'doctor') {
        window.location.href = 'login/login.html';
    }
}


